class Array
 {
  public static void main (String[] args)
  {
   int[] numbers;
   int[] primes = {2,3,5,7,11};
   int[] scores = new int[5];
   scores[0] = 95;
   scores[1] = 88;
   scores[2] = 72;
   scores[3] = 90;
   scores[4] = 85;
   System.out.println("Curly braces Integer Datatype");
   System.out.println(primes[3]);
   System.out.println("Square Bracket Integer Datatype");
   System.out.println(scores[4]);
   
   char[] character = {'s','v','#','*','@'};
   char[] letter = new char[5];
   letter[0] = 'k';
   letter[1] = 'd';
   letter[2] = '$';
   letter[3] = '&';
   letter[4] = '!';
   System.out.println("Curly braces Char Datatype");
   System.out.println(character[4]);
   System.out.println("Square Bracket Char Datatype");
   System.out.println(letter[2]);

   float[] decimal = {26.2f,57.5f,63.4f,79.3f,32.4f};
   float[] values = new float[5];
   values[0] = 9.4f;
   values[1] = 4.4f;
   values[2] = 64.6f;
   values[3] = 77.0f;
   values[4] = 60.1f;
   System.out.println("Curly braces float Datatype");
   System.out.println(decimal[3]);
   System.out.println("Square Bracket float Datatype");
   System.out.println(values[4]);

   String[] name = {"Karthiga","Durga","Sneha","Saranya","Vinothini"};
   String[] names = new String[5];
   names[0] = "Anuseetha";
   names[1] = "Dayana";
   names[2] = "Deepa";
   names[3] = "Mahintha";
   names[4] = "Saranya";
   System.out.println("Curly braces String Datatype");
   System.out.println(name[3]);
   System.out.println("Square Bracket String Datatype");
   System.out.println(names[2]);

  }
 }
   
   
